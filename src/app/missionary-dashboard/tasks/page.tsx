const taskTypeConfig = {
  thank_you: { label: 'Thank You', icon: Heart, color: 'text-rose-600 bg-rose-100 dark:bg-rose-950' },
  follow_up: { label: 'Follow Up', icon: User, color: 'text-blue-600 bg-blue-100 dark:bg-blue-950' },
  failed_payment: { label: 'Failed Payment', icon: AlertCircle, color: 'text-red-600 bg-red-100 dark:bg-red-950' },
  card_expiring: { label: 'Card Expiring', icon: CreditCard, color: 'text-amber-600 bg-amber-100 dark:bg-amber-950' },
  at_risk: { label: 'At Risk', icon: AlertTriangle, color: 'text-orange-600 bg-orange-100 dark:bg-orange-950' },
  custom: { label: 'Custom', icon: Sparkles, color: 'text-purple-600 bg-purple-100 dark:bg-purple-950' },
}

const priorityConfig = {
  high: { label: 'High', color: 'bg-rose-500' },
  medium: { label: 'Medium', color: 'bg-amber-500' },
  low: { label: 'Low', color: 'bg-blue-500' },
}

function TaskRow({ task, onComplete, onDismiss }: { task: Task; onComplete: () => void; onDismiss: () => void }) {
  const typeConfig = taskTypeConfig[task.taskType]
  const Icon = typeConfig.icon
  const isOverdue = task.dueDate && new Date(task.dueDate) < new Date() && task.status === 'pending'
  const isDueToday = task.dueDate && new Date(task.dueDate).toDateString() === new Date().toDateString()

  return (
    <div className={`flex items-start gap-4 p-4 border-b last:border-0 hover:bg-muted/50 transition-colors ${task.status === 'completed' ? 'opacity-60' : ''}`}>
      <Checkbox
        checked={task.status === 'completed'}
        onCheckedChange={() => onComplete()}
        className="mt-1"
      />
      
      <div className={`flex h-9 w-9 items-center justify-center rounded-lg ${typeConfig.color}`}>
        <Icon className="h-4 w-4" />
      </div>

      <div className="flex-1 min-w-0">
        <div className="flex items-center gap-2">
          <p className={`font-medium ${task.status === 'completed' ? 'line-through' : ''}`}>{task.title}</p>
          <div className={`h-2 w-2 rounded-full ${priorityConfig[task.priority].color}`} />
        </div>
        {task.description && (
          <p className="text-sm text-muted-foreground mt-0.5">{task.description}</p>
        )}
        <div className="flex items-center gap-3 mt-2">
          {task.donorName && (
            <span className="text-xs flex items-center gap-1 text-muted-foreground">
              <User className="h-3 w-3" />
              {task.donorName}
            </span>
          )}
          {task.dueDate && (
            <span className={`text-xs flex items-center gap-1 ${isOverdue ? 'text-rose-600 font-medium' : isDueToday ? 'text-amber-600 font-medium' : 'text-muted-foreground'}`}>
              <Clock className="h-3 w-3" />
              {isOverdue ? 'Overdue' : isDueToday ? 'Due today' : new Date(task.dueDate).toLocaleDateString()}
            </span>
          )}
          {task.autoGenerated && (
            <Badge variant="secondary" className="text-[10px] px-1.5 py-0">Auto</Badge>
          )}
        </div>
      </div>

      {task.status === 'pending' && task.donorName && (
        <div className="hidden md:flex gap-1">
          <Button variant="ghost" size="icon" className="h-8 w-8">
            <Mail className="h-4 w-4" />
          </Button>
          <Button variant="ghost" size="icon" className="h-8 w-8">
            <Phone className="h-4 w-4" />
          </Button>
        </div>
      )}

      <DropdownMenu>
        <DropdownMenuTrigger asChild>
          <Button variant="ghost" size="icon" className="h-8 w-8">
            <MoreHorizontal className="h-4 w-4" />
          </Button>
        </DropdownMenuTrigger>
        <DropdownMenuContent align="end">
          {task.status === 'pending' && (
            <>
              <DropdownMenuItem onClick={onComplete}>
                <CheckCircle2 className="mr-2 h-4 w-4" />
                Mark Complete
              </DropdownMenuItem>
              <DropdownMenuItem onClick={onDismiss}>
                <Trash2 className="mr-2 h-4 w-4" />
                Dismiss
              </DropdownMenuItem>
            </>
          )}
          {task.donorName && (
            <DropdownMenuItem>
              <User className="mr-2 h-4 w-4" />
              View Donor
            </DropdownMenuItem>
          )}
        </DropdownMenuContent>
      </DropdownMenu>
    </div>
  )
}

