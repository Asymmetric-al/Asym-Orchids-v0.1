export default function TasksPage() {
  const [taskList, setTaskList] = React.useState(tasks)
  const [filter, setFilter] = React.useState<'all' | 'pending' | 'completed'>('pending')
  const [typeFilter, setTypeFilter] = React.useState<string>('all')

  const handleComplete = (taskId: string) => {
    setTaskList(prev => prev.map(t => 
      t.id === taskId ? { ...t, status: t.status === 'completed' ? 'pending' : 'completed' as const } : t
    ))
  }

  const handleDismiss = (taskId: string) => {
    setTaskList(prev => prev.map(t => 
      t.id === taskId ? { ...t, status: 'dismissed' as const } : t
    ))
  }

  const filteredTasks = taskList.filter(task => {
    if (task.status === 'dismissed') return false
    if (filter === 'pending' && task.status !== 'pending') return false
    if (filter === 'completed' && task.status !== 'completed') return false
    if (typeFilter !== 'all' && task.taskType !== typeFilter) return false
    return true
  }).sort((a, b) => {
    if (a.status === 'completed' && b.status !== 'completed') return 1
    if (a.status !== 'completed' && b.status === 'completed') return -1
    if (a.priority === 'high' && b.priority !== 'high') return -1
    if (a.priority !== 'high' && b.priority === 'high') return 1
    return 0
  })

  const pendingCount = taskList.filter(t => t.status === 'pending').length
  const completedCount = taskList.filter(t => t.status === 'completed').length
  const overdueCount = taskList.filter(t => t.status === 'pending' && t.dueDate && new Date(t.dueDate) < new Date()).length

  return (
    <div className="space-y-6 p-4 md:p-6 lg:p-8">
      <div className="flex flex-col md:flex-row md:items-center md:justify-between gap-4">
        <div>
          <h1 className="text-2xl font-bold tracking-tight">Tasks</h1>
          <p className="text-muted-foreground">Manage your follow-up tasks and to-dos.</p>
        </div>
        <NewTaskDialog />
      </div>

      <div className="grid gap-4 md:grid-cols-4">
        <Card className="cursor-pointer hover:border-primary/50" onClick={() => setFilter('pending')}>
          <CardContent className="p-4 flex items-center gap-3">
            <div className="h-10 w-10 rounded-full bg-blue-100 dark:bg-blue-950 flex items-center justify-center">
              <Clock className="h-5 w-5 text-blue-600" />
            </div>
            <div>
              <p className="text-2xl font-bold">{pendingCount}</p>
              <p className="text-xs text-muted-foreground">Pending</p>
            </div>
          </CardContent>
        </Card>
        <Card className="cursor-pointer hover:border-emerald-500/50" onClick={() => setFilter('completed')}>
          <CardContent className="p-4 flex items-center gap-3">
            <div className="h-10 w-10 rounded-full bg-emerald-100 dark:bg-emerald-950 flex items-center justify-center">
              <CheckCircle2 className="h-5 w-5 text-emerald-600" />
            </div>
            <div>
              <p className="text-2xl font-bold">{completedCount}</p>
              <p className="text-xs text-muted-foreground">Completed</p>
            </div>
          </CardContent>
        </Card>
        <Card className={`${overdueCount > 0 ? 'border-rose-200 dark:border-rose-900/50' : ''}`}>
          <CardContent className="p-4 flex items-center gap-3">
            <div className="h-10 w-10 rounded-full bg-rose-100 dark:bg-rose-950 flex items-center justify-center">
              <AlertCircle className="h-5 w-5 text-rose-600" />
            </div>
            <div>
              <p className="text-2xl font-bold">{overdueCount}</p>
              <p className="text-xs text-muted-foreground">Overdue</p>
            </div>
          </CardContent>
        </Card>
        <Card>
          <CardContent className="p-4 flex items-center gap-3">
            <div className="h-10 w-10 rounded-full bg-amber-100 dark:bg-amber-950 flex items-center justify-center">
              <Calendar className="h-5 w-5 text-amber-600" />
            </div>
            <div>
              <p className="text-2xl font-bold">{taskList.filter(t => t.status === 'pending' && t.dueDate && new Date(t.dueDate).toDateString() === new Date().toDateString()).length}</p>
              <p className="text-xs text-muted-foreground">Due Today</p>
            </div>
          </CardContent>
        </Card>
      </div>

      <Card>
        <CardHeader className="pb-4">
          <div className="flex flex-col md:flex-row gap-4">
            <div className="flex gap-2">
              <Button 
                variant={filter === 'all' ? 'default' : 'outline'} 
                size="sm"
                onClick={() => setFilter('all')}
              >
                All
              </Button>
              <Button 
                variant={filter === 'pending' ? 'default' : 'outline'} 
                size="sm"
                onClick={() => setFilter('pending')}
              >
                Pending
              </Button>
              <Button 
                variant={filter === 'completed' ? 'default' : 'outline'} 
                size="sm"
                onClick={() => setFilter('completed')}
              >
                Completed
              </Button>
            </div>
            <Select value={typeFilter} onValueChange={setTypeFilter}>
              <SelectTrigger className="w-[160px]">
                <Filter className="h-4 w-4 mr-2" />
                <SelectValue placeholder="Task type" />
              </SelectTrigger>
              <SelectContent>
                <SelectItem value="all">All Types</SelectItem>
                <SelectItem value="thank_you">Thank You</SelectItem>
                <SelectItem value="at_risk">At Risk</SelectItem>
                <SelectItem value="card_expiring">Card Expiring</SelectItem>
                <SelectItem value="custom">Custom</SelectItem>
              </SelectContent>
            </Select>
          </div>
        </CardHeader>
        <CardContent className="p-0">
          {filteredTasks.length > 0 ? (
            <div>
              {filteredTasks.map(task => (
                <TaskRow 
                  key={task.id} 
                  task={task} 
                  onComplete={() => handleComplete(task.id)}
                  onDismiss={() => handleDismiss(task.id)}
                />
              ))}
            </div>
          ) : (
            <div className="p-8 text-center">
              <CheckCircle2 className="h-12 w-12 mx-auto text-emerald-500 mb-4" />
              <h3 className="font-medium mb-1">All caught up!</h3>
              <p className="text-sm text-muted-foreground">No pending tasks. Great job staying on top of your follow-ups!</p>
            </div>
          )}
        </CardContent>
      </Card>
    </div>
  )
}
